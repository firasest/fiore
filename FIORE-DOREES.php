										<h2>
											<a>FIORE DORÉES</a>
										</h2>
										<p style="text-align: justify;">
											<strong>Ingrédients</strong><br>
											<div class="row" style="margin-left: 50px;text-align: justify;">
															
														<div class="col-sm-6">

															Ingrédients pour la pâte: <br>
															1kg de farine<br>
															1 ouf <br>
															10g d'huile <br>
															8g de sel <br>
															un peu d'eau.
														</div>

														<div class="col-sm-6">

															Ingrédients pour la farce: <br>
															250g de Cacciotta Di Fiore<br>
															50g de Vecchio Bastardo Di Fiore <br>
															1 bouquet de persil <br>
															sel et poivre du moulin.
														</div>

														


														</div>

											<strong>Préparation</strong><br>
											<div style="margin-left: 50px;text-align: justify;">

														<div style="text-decoration: underline">La veille : </div> <br>

														Mélanger les ingrédients de la pâte jusqu'a former une jolie boule bien lisse. L'envelopper dans un linge propre et fariné, puis laisser reposer la pâte un minimum de 6 heures, un maximum de 12. <br><br>

														<div style="text-decoration: underline">Le lendemain : </div> <br>

														Allumer votre four a 220° (Thermostat 8) Laver puis hacher grossierement le persil. Râper le Cacciotta Di Fiore et émietter le Vecchio Bastardo Di Fiore.<br>
														 Mélanger les trois ingrédients, saler, poivrer et former 30 petits tas égaux de 10g chacun. Sortir la pâte, l'étaler a la main et la couper en 30 parties égales.<br>
														  Les étaler une a une au rouleau en fines galettes plates.<br>
														   Mouiller leurs pourtours avec de l'eau, placer chaque petit tas de fromages au centre de chacune d'elles, refermer en deux comme des ravioles, décorer de sésames et les placer l'une apres l'autre sur une plaque préalablement beurrée.<br>
														    Enfourner une vingtaine de minutes les servir soit a l'apéritif soit autour d'une jolie salade verte.
											</div>
										</p>
